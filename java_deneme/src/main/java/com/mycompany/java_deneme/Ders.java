package com.mycompany.java_deneme;


public class Ders {
    private int derskodu;
    private String dersadi;
    private int sinifi;
    private String donemi;
    private double kredi;

    /**
     * @return the derskodu
     */
    public int getDerskodu() {
        return derskodu;
    }

    /**
     * @param derskodu the derskodu to set
     */
    public void setDerskodu(int derskodu) {
        this.derskodu = derskodu;
    }

    /**
     * @return the dersadi
     */
    public String getDersadi() {
        return dersadi;
    }

    /**
     * @param dersadi the dersadi to set
     */
    public void setDersadi(String dersadi) {
        this.dersadi = dersadi;
    }

    /**
     * @return the sinifi
     */
    public int getSinifi() {
        return sinifi;
    }

    /**
     * @param sinifi the sinifi to set
     */
    public void setSinifi(int sinifi) {
        this.sinifi = sinifi;
    }

    /**
     * @return the donemi
     */
    public String getDonemi() {
        return donemi;
    }

    /**
     * @param donemi the donemi to set
     */
    public void setDonemi(String donemi) {
        this.donemi = donemi;
    }

    /**
     * @return the kredi
     */
    public double getKredi() {
        return kredi;
    }

    /**
     * @param kredi the kredi to set
     */
    public void setKredi(double kredi) {
        this.kredi = kredi;
    }
}
